from Crypto.Util.number import *
import random
flag = bytes_to_long(b'CTF{???}')

while 1:
    a,b = random.randint(1,2**64), random.randint(1,2**64)
    p = a**5 + 31*a + 1337
    q = b**5 + 31*b + 1337
    if isPrime(p) and isPrime(q):
        break

n = p*q
print(pow(flag,0x10001, n))
print(n)
